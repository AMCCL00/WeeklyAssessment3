package com.company.Question_1;

public class FavoriteColorAndFavoritePrimaryColor {

    private String blue = "Blue";
    private String red = "Red";

    public FavoriteColorAndFavoritePrimaryColor(String blue, String red) {
        this.blue = blue;
        this.red = red;

    }

    public String getBlue() {
        return blue;
    }

    public void setBlue(String blue) {
        this.blue = blue;
    }

    public String getRed() {
        return red;
    }

    public void setRed(String red) {
        this.red = red;
    }

}